package org.openhab.binding.oceanic.internal;

import org.osgi.framework.BundleActivator;
import org.osgi.framework.BundleContext;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

/**
 * Extension of the default OSGi bundle activator
 * 
 * @author Karel Goderis
 * @since 1.5.0
 */

public class OceanicActivator implements BundleActivator {
	
	private static Logger logger = LoggerFactory.getLogger(OceanicActivator.class);

	private static BundleContext context;

	static BundleContext getContext() {
		return context;
	}

	/*
	 * (non-Javadoc)
	 * @see org.osgi.framework.BundleActivator#start(org.osgi.framework.BundleContext)
	 */
	public void start(BundleContext bundleContext) throws Exception {
		logger.debug("Oceanic binding has been started.");

		OceanicActivator.context = bundleContext;
	}

	/*
	 * (non-Javadoc)
	 * @see org.osgi.framework.BundleActivator#stop(org.osgi.framework.BundleContext)
	 */
	public void stop(BundleContext bundleContext) throws Exception {
		logger.debug("Oceanic binding has been stopped.");

		OceanicActivator.context = null;
	}

}
