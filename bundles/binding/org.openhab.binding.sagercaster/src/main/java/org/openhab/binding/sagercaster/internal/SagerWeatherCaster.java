		public void sagerNubesUpdate() {
			int result;
			if (!this.raining) {
				if (cloudLevel > 80) result = 4; 		//overcast
				else if (cloudLevel > 50) result = 3; 	// mostly overcast
				else if (cloudLevel > 20) result = 2; 	// partly cloudy
				else result = 1;						// clear
			} else
				result = 5; 							// raining
			if (result != this.nubes) {
				this.nubes = result;
				updatePrediction();
			}
		}
		
		public static int sagerPressureLevel(double current) {
			int result = 1;
			if (current > 1029.46) { result = 1; }
			else if (current > 1019.3) { result = 2; }
			else if (current > 1012.53) { result = 3; }
			else if (current > 1005.76) { result = 4; }
			else if (current > 999) { result = 5; }
			else if (current > 988.8) { result = 6; }
			else if (current > 975.28) { result = 7; }
			else { result = 8; }
			return result;
		}
		
		public static int sagerPressureTrend(double current, double historic) {
			double evol = current - historic;
			int result = 0;
			
			if (evol > 1.4) result = 1; 				// Rising Rapidly
			else if (evol > 0.68) result = 2; 			// Rising Slowly
			else if (evol > -0.68) result = 3; 			// Normal
			else if (evol > -1.4) result = 4; 			// Decreasing Slowly
			else result = 5; 							// Decreasing Rapidly
			
			return result;
		}
		
		public static int sagerWindTrend(double historic, double position) {
			int result = 1;								// Steady
			double angle = 180 - Math.abs(Math.abs(position-historic) - 180);
			if (angle > 45) { 
				int evol = (int) (historic + angle);
				if (evol > 360) { evol = evol - 360; }
				if (evol==position) {
					result = 2;							// Veering
				} else {
					result = 3;							// Backing
				}
			}
			return result;
		}
		
		public String getCompass() {
			double step = 360.0 / NTZDIRECTIONS.length;
			double b = Math.floor((this.currentBearing + (step / 2.0)) / step);
			return NTZDIRECTIONS[(int) (b % NTZDIRECTIONS.length)];
		}
		
		private void updatePrediction() {
			int zWind = Arrays.asList(usedDirections).indexOf(getCompass());
			String d1 = "-";
			
			if (zWind == 0) { 	
				if (windEvolution == 3) d1 ="A";
				else if (windEvolution == 1) d1 ="B";
				else if (windEvolution == 2) d1 ="C";
			} else if (zWind == 1) {
				if (windEvolution == 3) d1 ="D";
				else if (windEvolution == 1) d1 ="E";
				else if (windEvolution == 2) d1 ="F";
			} else if (zWind == 2) {
				if (windEvolution == 3) d1 ="G";
				else if (windEvolution == 1) d1 ="H";
				else if (windEvolution == 2) d1 ="J";
			} else if (zWind == 3) {
				if (windEvolution == 3) d1 ="K";
				else if (windEvolution == 1) d1 ="L";
				else if (windEvolution == 2) d1 ="M";
			} else if (zWind == 4) {
				if (windEvolution == 3) d1 ="N";
				else if (windEvolution == 1) d1 ="O";
				else if (windEvolution == 2) d1 ="P";
			} else if (zWind == 5) {
				if (windEvolution == 3) d1 ="Q";
				else if (windEvolution == 1) d1 ="R";
				else if (windEvolution == 2) d1 ="S";
			} else if (zWind == 6) {
				if (windEvolution == 3) d1 ="T";
				else if (windEvolution == 1) d1 ="U";
				else if (windEvolution == 2) d1 ="V";
			} else if (zWind == 7) {
				if (windEvolution == 3) d1 ="W";
				else if (windEvolution == 1) d1 ="X";
				else if (windEvolution == 2)d1 ="Y";
			} else if (currentBeaufort == 0) {
				d1 ="Z";
			}
			
			String forecast = sageForecaster.get(d1 + String.valueOf(sagerPressure) + String.valueOf(pressureEvolution) + String.valueOf(nubes));
			if (forecast != null) {
				prevision = new Prevision(forecast);
			} else
				prevision = null;
		}
			
